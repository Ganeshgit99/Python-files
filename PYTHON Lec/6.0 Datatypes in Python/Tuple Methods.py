'''
tuple method
=============
1)count():This gives occurance of value in the given tuple.
        syntax:tuple_variable.count(value)
2)index():It returns index of first occurance of element in tuple.
        synatx:tuple_variable.index(value)

'''

t=(1,2,20,10,4,10,2,10,98,10,2)
n=t.count(2)
print(n)

index=t.index(4)
print(index)
